package Identificadores;

public class EjemploIdentificadores {

	public static void main(String[] args) {
		
		/*Identificador = cualquier cosa que yo nombre en JAVA
		 * 
		 * Un identificador debe de iniciar con una letra, un _ o un $
		 * 
		 * */
		
		String identificador = null;
		String _identificador = null;
		String $identificador = null;
		
		/*
		 * Despues del primer carater puedo tener una combinacion entre numeros, letras y los simbolos permitidos
		 * */
		
		String idasa$2sdf4entificador = null;
		String _i$2sdf4dentificador = null;
		String $identif$2sdf4icador = null; 
		
		/*
		 * 
		 * Los identificadores no tienen limite de longitud
		 * */
		
		
		String nombreDeLaPersonaACargoDelCobro = null;
		
		/*
		 * Los identificadores no deben ser palabras reservadas
		 * 
		 * */
		
		String foo = "";
		String FOO = "";
		String Foo = "";
		String fOo = "";
		
		
		/*
		 * Ofuscamiento del codigo
		 * */
		Integer $_$ = 3;
		
		$ _ = new $();
		$_$ = _.$3_($_$);
		System.out.println($_$);
		
	}
	
}

